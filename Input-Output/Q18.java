import java.util.*;

public class Q18 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        System.out.print("Enter day: ");
        int dd = sc.nextInt();

        System.out.print("Enter month: ");
        int mm = sc.nextInt();

        System.out.print("Enter year: ");
        int yyyy = sc.nextInt();

        System.out.println(dd + "/" + mm + "/" + yyyy);
    }
}
