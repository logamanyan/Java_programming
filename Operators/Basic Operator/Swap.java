import java.util.*;
public class Swap {
    
    public static void main(String[] args)
    {
        int a,b,c;
        Scanner s=new Scanner(System.in);
        a = s.nextInt();
        b=s.nextInt();
        c=a;
        a=b;
        b=c;
        System.out.println("The value of a is "+a);
        System.out.println("The value of b is "+b);
        


    }
}
